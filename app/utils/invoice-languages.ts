export interface InvoiceLanguage {
  id: string
  name: string
  flag: string
  texts: {
    // Headers and sections
    invoice: string
    paymentInformation: string
    notes: string

    // Invoice details
    issueDate: string
    dueDate: string
    referenceNumber: string
    customerReference: string
    orderNumber: string

    // Company information
    companyId: string
    vatId: string
    email: string
    website: string

    // Payment information
    paymentMethod: string
    bankAccount: string
    iban: string
    swiftBic: string

    // Payment methods
    paymentMethods: {
      cash: string
      bankTransfer: string
      paypal: string
      creditCard: string
      wise: string
      unknown: string
    }

    // Tax type labels (for PDF)
    taxTypes: {
      vat: { percent: string; amount: string; total: string; id: string }
      gst: { percent: string; amount: string; total: string; id: string }
      sales_tax: { percent: string; amount: string; total: string; id: string }
      none: { percent: string; amount: string; total: string; id: string }
    }

    // Table headers
    table: {
      description: string
      quantity: string
      unitPrice: string
      vatPercent: string
      netPrice: string
      vatAmount: string
      total: string
    }

    // Totals
    netTotal: string
    vatTotal: string
    totalDue: string
    paid: string

    // Reverse Charge
    reverseChargeText: string

    // Footer
    generatedBy: string
  }
}

